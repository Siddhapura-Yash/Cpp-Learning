#include <iostream>
using namespace std;

int main() {
	int a = 42;           // integer, whole number
	char c = 'A';         // single character, in single quotes
	float f = 3.14f;      // floating-point number, 4 bytes
	double d = 3.141592;  // double-precision floating-point, 8 bytes

	cout << "integer is : " << a << endl;
	cout << "character is : " << c << endl;
	cout << "float is : " << f << endl;
	cout << "double is : " << d << endl;
}